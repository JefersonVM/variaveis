let x = 10, y = 5;

let resultado = x + y;

console.log(`A soma de ${x} e ${y} é ${resultado}`); // 15

resultado = x - y;

console.log(`A subtração de ${x} e ${y} é ${resultado}`); // 5

resultado = x * y;

console.log(`A multiplicação de ${x} e ${y} é ${resultado}`); // 50

resultado = x / y;

console.log(`A divisão de ${x} e ${y} é ${resultado}`); // 2